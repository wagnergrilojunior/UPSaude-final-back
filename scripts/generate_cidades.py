def gerar_sql_cidades():
    """Gera SQL para inserção de cidades por estado."""
    sql_lines = []
    
    for sigla, cidades in ESTADOS_CIDADES.items():
        sql_lines.append(f"\n-- ========== {sigla} - {len(cidades)} principais cidades ==========\n")
        
        for nome, codigo_ibge, lat, lon in cidades:
            # Escapar aspas simples nos nomes das cidades (substituir ' por '')
            nome_escaped = nome.replace("'", "''")
            sql = f"""-- {nome}
INSERT INTO public.cidades (id, criado_em, atualizado_em, ativo, nome, codigo_ibge, latitude, longitude, estado_id)
SELECT gen_random_uuid(), NOW(), NOW(), true, '{nome_escaped}', '{codigo_ibge}', {lat}, {lon},
    (SELECT id FROM public.estados WHERE sigla = '{sigla}' LIMIT 1)
WHERE NOT EXISTS (SELECT 1 FROM public.cidades WHERE codigo_ibge = '{codigo_ibge}');
"""
            sql_lines.append(sql)
    
    return '\n'.join(sql_lines)

